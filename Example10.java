public class Example10 {
    public static void main(String[] args){
        int myNumber = 5;
        switch (myNumber){
            case 1:
                System.out.println("One");
                break;
            case 2:
                System.out.println("Two");
                break;
            case 3:
                System.out.println("Three");
                break;
            default:
                System.out.println("Invalid Number");
                break;
        }
    }
}
